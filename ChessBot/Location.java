
public class Location
{
    private int xPos;
    private int yPos;
    
    public Location(int xCoord, int yCoord) {
        this.xPos = xCoord;
        this.yPos = yCoord;
    }
    
    public int getXPos() {
        return xPos;
    }
    
    public int getYPos() {
        return yPos;
    }
}
